class Cache:

    __shared_state = { 'cache' : {} }

    def __init__ (self):
        self.__dict__ = self.__shared_state
        
    def insert (self, key, value):
        self.__dict__['cache'][key] = value

    def lookup (self, key):
        return self.__dict__['cache'].get (key, None)

    
